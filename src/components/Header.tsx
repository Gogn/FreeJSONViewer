interface HeaderProps {
  theme: Theme;
  onThemeToggle: () => void;
  onSettingsOpen: () => void;
  onAboutOpen: () => void;
  onSupportOpen: () => void;
  activeTab: ActiveTab;
  onTabChange: (tab: ActiveTab) => void;
  hasError: boolean;
  errorMode: ErrorMode;
}

export default function Header({
  theme,
  onThemeToggle,
  onSettingsOpen,
  onAboutOpen,
  onSupportOpen,
  activeTab,
  onTabChange,
  hasError,
  errorMode,
}: HeaderProps) {
  const treeDisabled = hasError && errorMode === 'disable';

  return (
    <header className="header">
      <span className="header-title">JSON Viewer</span>

      <div className="header-separator" />

      <div className="header-tabs">
        <button
          className={`tab-btn${activeTab === 'raw' ? ' active' : ''}`}
          onClick={() => onTabChange('raw')}
        >
          Raw Text
        </button>
        <button
          className={`tab-btn${activeTab === 'tree' ? ' active' : ''}`}
          onClick={() => !treeDisabled && onTabChange('tree')}
          disabled={treeDisabled}
          title={treeDisabled ? 'Fix JSON errors to enable tree view' : undefined}
        >
          Tree Viewer
        </button>
      </div>

      <div className="header-actions">
        <button className="support-header-btn" onClick={onSupportOpen} title="Support this project">
          <svg
            width="13"
            height="13"
            viewBox="0 0 24 24"
            fill="currentColor"
            stroke="none"
            aria-hidden="true"
          >
            <path d="M12 21.35l-1.45-1.32C5.4 15.36 2 12.28 2 8.5 2 5.42 4.42 3 7.5 3c1.74 0 3.41.81 4.5 2.09C13.09 3.81 14.76 3 16.5 3 19.58 3 22 5.42 22 8.5c0 3.78-3.4 6.86-8.55 11.54L12 21.35z" />
          </svg>
          Support
        </button>
        {import.meta.env.VITE_GITHUB_URL && (
          <a
            className="icon-btn"
            href={import.meta.env.VITE_GITHUB_URL}
            target="_blank"
            rel="noopener noreferrer"
            title="View on GitHub"
          >
            <GitHubIcon />
          </a>
        )}
        <button className="icon-btn" onClick={onAboutOpen} title="About">
          <svg
            width="15"
            height="15"
            viewBox="0 0 24 24"
            fill="none"
            stroke="currentColor"
            strokeWidth="2"
            strokeLinecap="round"
          >
            <circle cx="12" cy="12" r="10" />
            <line x1="12" y1="16" x2="12" y2="12" />
            <line x1="12" y1="8" x2="12.01" y2="8" />
          </svg>
        </button>
        <button
          className="icon-btn"
          onClick={onThemeToggle}
          title={theme === 'dark' ? 'Switch to light mode' : 'Switch to dark mode'}
        >
          {theme === 'dark' ? <SunIcon /> : <MoonIcon />}
        </button>
        <button className="icon-btn" onClick={onSettingsOpen} title="Settings">
          <GearIcon />
        </button>
      </div>
    </header>
  );
}
